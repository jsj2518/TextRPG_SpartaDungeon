    enum SE //Scene Event
    {
        MainMenu_Default,
        MainMenu_Load,
        IngameMenu_Default,
        Void_Opening,
        Town_Default,
        Status_Default,
        Inventory_Inventory,
        Inventory_Gear,
        Inventory_Skill,
        Shop_GearShop,
        Shop_PotionShop,
        Shop_Mercenary,
        Inn_Default,
        Max
    }
    class SceneEvent
    {
        SE sceneCurrent;

        public SceneEvent(SE seNum)
        {
            sceneCurrent = seNum;
        }

        //이벤트 씬 구동
        public bool Execute(out SB SB_Next, out SE SE_Next, ScreenController screenController, KeyController keyController)
        {
            if (GameData.ExistsData(1)) Program.gameData1 = GameLoader.LoadData(1);
            if (GameData.ExistsData(2)) Program.gameData2 = GameLoader.LoadData(2);
            if (GameData.ExistsData(3)) Program.gameData3 = GameLoader.LoadData(3);

            SB_Next = SB.MainMenu;
            SE_Next = SE.MainMenu_Default;

            int selectIdx;
            int cheatActivate;
            int i, intVal, tab;
            string strInput;
            string[] conversationPreset;
            bool boolVal, confirm;

            ConsoleKey[] keyFilter;
            ConsoleKey keyInput;

            switch (sceneCurrent)
            {
                //메인메뉴
                case SE.MainMenu_Default:
                    #region
                    screenController.Write(7, 33, 34, "             새 게임              ");
                    screenController.Write(13, 33, 34, "             불러오기             ");
                    screenController.Write(19, 33, 34, "             게임종료             ");
                    screenController.Write(23, 76, 8, "Z : 확인");
                    selectIdx = 0;
                    keyFilter = new ConsoleKey[] { ConsoleKey.UpArrow, ConsoleKey.DownArrow, ConsoleKey.Z };

                    while (true) //메뉴 선택
                    {
                        keyInput = keyController.GetUserInput(keyFilter, out cheatActivate);
                        switch (keyInput)
                        {
                            case ConsoleKey.UpArrow:
                                selectIdx = (selectIdx == 0) ? 0 : selectIdx - 1;
                                break;
                            case ConsoleKey.DownArrow:
                                selectIdx = (selectIdx == 2) ? 2 : selectIdx + 1;
                                break;
                            case ConsoleKey.Z:
                                switch (selectIdx)
                                {
                                    case 0: //새 게임
                                        SB_Next = SB.Void;
                                        SE_Next = SE.Void_Opening;
                                        return true;
                                    case 1: //불러오기
                                        SB_Next = SB.MainMenu;
                                        SE_Next = SE.MainMenu_Load;
                                        return true;
                                    case 2: //게임 종료
                                        SB_Next = SB.MainMenu;
                                        SE_Next = SE.MainMenu_Default;
                                        return false;
                                }
                                break;
                        }
                        switch (selectIdx)
                        {
                            case 0:
                                screenController.Write(7, 28, 3, "-->");
                                screenController.Write(13, 28, 3, "   ");
                                screenController.Write(19, 28, 3, "   ");
                                break;
                            case 1:
                                screenController.Write(7, 28, 3, "   ");
                                screenController.Write(13, 28, 3, "-->");
                                screenController.Write(19, 28, 3, "   ");
                                break;
                            case 2:
                                screenController.Write(7, 28, 3, "   ");
                                screenController.Write(13, 28, 3, "   ");
                                screenController.Write(19, 28, 3, "-->");
                                break;
                        }
                        screenController.Update();
                    }
                #endregion
                //저장파일 불러오기
                case SE.MainMenu_Load:
                    #region
                    if (Program.gameData1 != null) screenController.Write(7, 33, 34, string.Format("  LV {0,2}   {1,-14}   {2}   ", Program.gameData1.PP_Level, Program.gameData1.PP_Name, Program.gameData1.PP_ClassKor));
                    else screenController.Write(7, 33, 34, "           데이터 없음            ");

                    if (Program.gameData2 != null) screenController.Write(13, 33, 34, string.Format("  LV {0,2}   {1,-14}   {2}   ", Program.gameData2.PP_Level, Program.gameData2.PP_Name, Program.gameData2.PP_ClassKor));
                    else screenController.Write(13, 33, 34, "           데이터 없음            ");

                    if (Program.gameData3 != null) screenController.Write(19, 33, 34, string.Format("  LV {0,2}   {1,-14}   {2}   ", Program.gameData3.PP_Level, Program.gameData3.PP_Name, Program.gameData3.PP_ClassKor));
                    else screenController.Write(19, 33, 34, "           데이터 없음            ");

                    screenController.Write(23, 76, 19, "Z : 확인   X : 뒤로");

                    selectIdx = 0;
                    keyFilter = new ConsoleKey[] { ConsoleKey.UpArrow, ConsoleKey.DownArrow, ConsoleKey.Z, ConsoleKey.X };

